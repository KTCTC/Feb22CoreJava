package data.types;

public class DataTypeClass {

	public static void main(String[] args) {
		System.out.println("-------------------------------- char");
		char ccVariable = 65;
		System.out.println(ccVariable);		
		
		char ch = 'r';
		System.out.println(ch);
		
		System.out.println("-------------------------------- boolean");
		boolean blVariable = true;		
		System.out.println(blVariable);
		blVariable = false;
		System.out.println(blVariable);
		
		System.out.println("-------------------------------- double");		
		double dbVariable = 45.67d;
		System.out.println(dbVariable);
		
		System.out.println("-------------------------------- float");
		
		float flVariable = 56.67f;
		float fl2 ;
		fl2 = flVariable;
		
		System.out.println(flVariable);
		
		System.out.println("-------------------------------- long");		
		long longVariable = 7886778987l;
		System.out.println(longVariable);
		
		System.out.println("-------------------------------- int");
		int intVariable = 888987646;
		
		int abcd;
		abcd = 999;
		intVariable = abcd;
		System.out.println(intVariable);
		
		System.out.println("-------------------------------- short");
		short sh;
		sh = 330;
		System.out.println(sh);
		
		System.out.println("-------------------------------- byte");
		byte bt;
		byte byteVariable;
		
		byte btVariable2 = 56;
		System.out.println(btVariable2);
		bt = 90;
		byteVariable = bt;
		System.out.println(bt);
		System.out.println(byteVariable);
		byteVariable = 88;
		bt = 77;
		System.out.println(bt);
		System.out.println(byteVariable);
	}

}
